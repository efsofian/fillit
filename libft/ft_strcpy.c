#include <string.h>

char	*ft_strcpy(char *dest, char *arc)
{
	size_t	i;

	i = 0;
	while (arc[i] != '\0')
	{
		dest[i] = arc[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
